#include "chatbot.h"

char *my_strncpy(char *dest, char *src, int n)
{
    int z = 0;

    while (src[z] != '\0' && z < n) {
        dest[z] = src[z];
        z = z + 1;
        dest[z] = '\0';
    }
    return dest;
}

int my_strlen(char *str)
{
    int z = 0;

    while (str[z] != '\0') {
        z = z + 1;
    }
    return z;
}